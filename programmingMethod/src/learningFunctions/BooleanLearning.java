package learningFunctions;

public class BooleanLearning {
	
	public static void main(String[] args) {
		String name = "Sanaa";
		boolean isName;
		
		if(name == "anaa") {
			System.out.println("I am Sanaa");
			isName = true;
			System.out.println(isName);
		}else {
			System.out.println("I am not sanaa");
			isName = false;
			System.out.println(isName);
		}
		
		
	}

}
